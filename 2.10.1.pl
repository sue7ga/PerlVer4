use strict;
use warnings;

my @day = qw(ark dip wap sen pop sep kir);
my @month = qw(diz pod bod rod sip wax lin sen kun fiz nap dep);

sub number_to_day_name{
 my $num = shift if @_;
 return $day[$num]; 
}

sub number_to_month_name{
 my $num = shift if @_;
 return $month[$num];
}

print  number_to_day_name(4);
print  number_to_month_name(5);

